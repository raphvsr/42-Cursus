#include "ft_printf.h"

size_t	ft_putnbr(int n)
{
	size_t	size;

	size = 0;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
		size++;
	}
	if (n >= 10)
		size += ft_putnbr(n / 10);
	ft_putchar((n % 10) + '0');
	return (size + 1);
}
